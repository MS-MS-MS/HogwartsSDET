class cacl():
    def add(self,a,b):
        return a+b

    def jian(self,a,b):
        return a-b

    def cheng(self,a,b):
        return a*b

    def chu(self,a,b):
        return a/b

class Calculator:
    def add(self, a, b):
        return a + b

    def sub(self, a, b):
        return a - b

    def mul(self, a, b):
        return a * b

    def div(self, a, b):
        return a / b
